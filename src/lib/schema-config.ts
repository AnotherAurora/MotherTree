import type { EnumName, TableName } from "@/lib/database.types";
import { formatLabel } from "@/lib/utils";

export type FieldType =
  | "id"
  | "text"
  | "dotSeparated"
  | "number"
  | "boolean"
  | "textarea"
  | "enum"
  | "foreignKey"
  | "timestamp";

export type ForeignKeyRef = {
  table: TableName;
  displayColumn: string;
  /** Composite label for complex parent rows */
  labelKind?: "manifestation";
  /** Filter options to rows where column equals the named form field value */
  filterBy?: { column: string; formField: string };
};

export type FieldConfig = {
  name: string;
  label: string;
  type: FieldType;
  enumName?: EnumName;
  foreignKey?: ForeignKeyRef;
  /** Hide from create/edit forms */
  formHidden?: boolean;
  /** Hide from list table */
  listHidden?: boolean;
  /** Database generates on insert (omit from create forms) */
  autoGenerated?: boolean;
  /** Populated at read time, not stored on the table */
  computed?: boolean;
  required?: boolean;
  defaultValue?: string | number | boolean | null;
  /** Allow click-to-edit directly in the list table */
  inlineEditable?: boolean;
};

export type ChildTableConfig = {
  table: TableName;
  label: string;
  /** Parent column whose value is copied into this FK on child rows */
  parentKeyColumn: string;
  /** Child FK column that receives the parent key */
  foreignKeyColumn: string;
  fields: FieldConfig[];
};

export type TableConfig = {
  name: TableName;
  label: string;
  description: string;
  /** Column used when this table appears in FK dropdowns */
  displayColumn: string;
  softDelete: boolean;
  /** Order in sidebar (lower = higher) */
  order: number;
  /** Omit from sidebar navigation (managed via a parent table form) */
  sidebarHidden?: boolean;
  /** Child rows edited inline on this table's create/edit form */
  childTables?: ChildTableConfig[];
  uniqueConstraints?: {
    fields: string[];
    ignoreDeleted?: boolean;
    message?: string;
  }[];
  /** Optional alternate list layouts (default: table only) */
  listViews?: {
    tree?: { pathField: string };
  };
  fields: FieldConfig[];
};

